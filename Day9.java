package coding100days;

public class Day9 {

    public static void main(String[] args) {

        // deklarasi saja
        String nama;
        int nilai;
        

        // inisialisasi (beri nilai pertama kali)
        nama = "Zaskia";
        nilai = 4;
        System.out.println("Nama awal: " + nama);
        System.out.println("Nilai awal: " + nilai);

        // update (ubah nilainya)
        nama = "Kia";
        nilai = 2;
        System.out.println("Nama setelah update: " + nama);
        System.out.println("Nilai setelah update: " + nilai);
    }
}


