package coding100days;

public class Day25 {

    public static void main(String[] args) {

        int a = 7;
        int b = 12;

        System.out.println("Nilai awal: a = " + a + ", b = " + b);

        a++;
        b--;

        System.out.println("Setelah a++ : a = " + a);
        System.out.println("Setelah b-- : b = " + b);
    }
}
